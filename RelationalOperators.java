public class RelationalOperators {
    public static void main(String[] args) {
        // Relational Operators
        // < , > , == , != , <= , >=
        // *return a" boolean" result , true or false
        int a = 14, b = 45;
        System.out.println("a = " + a + ", b = " + b);
        System.out.println("a == b  is " + (a == b));
        System.out.println("a != b  is " + (a != b));
        System.out.println("a >= b  is " + (a >= b));
        System.out.println("a <= b  is " + (a <= b));
        System.out.println("a < b   is " + (a < b));
        System.out.println("a > b   is " + (a > b));
    }

}
